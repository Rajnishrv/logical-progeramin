public class Num{
    public static void main(String[] args) {
        int binary = 0b101; //2
        int hexa = 0x3465;  //16
        int octa = 0105;    //8
        System.out.println("Binary: "+binary);
        System.out.println("Hexa: " +hexa);
        System.out.println("Hexa: " +octa);
    }
}